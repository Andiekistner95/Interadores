package exercicio.numero14.itemc;

public class Classe {
	public static void main(String[] args) {

		for (int aux = 1; aux <= 3; aux++) {
			switch (aux) {
			case 1:
				System.out.println("a");
				break;
			case 2:
				System.out.println("b");
				break;
			case 3:
				System.out.println("c");
				break;
			}
		}
	}
}
